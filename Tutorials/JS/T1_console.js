// Use console.log(); for printing
// It is not necessary to put semicolon at end of lines.

console.time('Your code takes time');
console.log("Hello World");
console.log(4 + 34);
console.log(true);
console.log([1, 2, 3])
console.log({ 'subject': 'maths', 'marks': 50 });
console.table({ 'subject': 'maths', 'marks': 50 }); // Makes a table
console.warn("This is a warning"); // Throw a warning
// console.clear(); // Clear the console
console.timeEnd('Your code takes time'); // For printing time difference
console.assert(500 < 185, "This is not possible");
console.error("This is an error"); // Will throw an error on console