console.log(Math); // Printing Math Object

// Some Important Functions
console.log(Math.E);
console.log(Math.PI);

console.log(Math.round(5.2)); // 5
console.log(Math.round(5.5)); // 6
console.log(Math.round(5.7)); // 6
console.log(Math.round(-5.7)); // -6
console.log(Math.round(-5.3)); // -5

console.log(Math.ceil(5.2)); // 6
console.log(Math.ceil(5.5)); // 6
console.log(Math.ceil(5.7)); // 6
console.log(Math.ceil(-5.7)); // -5

console.log(Math.floor(5.2)); // 5
console.log(Math.floor(5.5)); // 5
console.log(Math.floor(5.7)); // 5
console.log(Math.floor(-5.3)); // -6

console.log(Math.abs(-5)); // 5
console.log(Math.abs(5)); // 5

console.log(Math.sqrt(9)); // 3
console.log(Math.sqrt(-9)); // NaN

console.log(Math.pow(2, 5)); // 32

console.log(Math.min(5, 4, 8, 1, 8, 2)); // 1
console.log(Math.max(5, 4, 8, 1, 8, 2)); // 8

console.log(Math.random()); // b/w 0 to 1
console.log(Math.random() * 100); // b/w 0 to 100
console.log((Math.random() * 10) + 10); // b/w 10 to 20