const age = 20;

switch (age) {
    case 18:
        console.log("You are 18");
        break;
    case 20:
        console.log("You are 20");
        break;
    case 16:
        console.log("You are 16");
        break;
    default: // If no case matches
        console.log("Age is unknown.");
        break;
}